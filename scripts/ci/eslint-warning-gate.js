#!/usr/bin/env node
/* eslint-disable no-console */
const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const REPORT_PATH = path.join(ROOT, '.ci', 'eslint-report.json');
const BASELINE_PATH = path.join(ROOT, '.ci', 'eslint-warning-baseline.json');
const WRITE_BASELINE = process.argv.includes('--write-baseline');

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function normalizeFilePath(filePath) {
  return filePath.replace(`${ROOT}${path.sep}`, '').replace(/\\/g, '/');
}

function toWarningMap(eslintReport) {
  const map = new Map();
  for (const fileResult of eslintReport) {
    const filePath = normalizeFilePath(fileResult.filePath || '');
    const messages = Array.isArray(fileResult.messages) ? fileResult.messages : [];
    for (const message of messages) {
      if (message.severity !== 1) continue;
      const ruleId = message.ruleId || 'unknown-rule';
      const key = `${filePath}::${ruleId}`;
      map.set(key, (map.get(key) || 0) + 1);
    }
  }
  return map;
}

function mapToObject(map) {
  return Object.fromEntries([...map.entries()].sort((a, b) => a[0].localeCompare(b[0])));
}

if (!fs.existsSync(REPORT_PATH)) {
  console.error(`Missing eslint report at ${REPORT_PATH}.`);
  process.exit(2);
}

const report = readJson(REPORT_PATH);
const currentMap = toWarningMap(report);

if (WRITE_BASELINE) {
  fs.mkdirSync(path.dirname(BASELINE_PATH), { recursive: true });
  fs.writeFileSync(
    BASELINE_PATH,
    `${JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        note: 'Autogenerated by scripts/ci/eslint-warning-gate.js --write-baseline',
        warnings: mapToObject(currentMap),
      },
      null,
      2
    )}\n`
  );
  console.log(`Wrote ESLint warning baseline to ${BASELINE_PATH}`);
  process.exit(0);
}

if (!fs.existsSync(BASELINE_PATH)) {
  console.error(`Missing baseline at ${BASELINE_PATH}. Run with --write-baseline first.`);
  process.exit(2);
}

const baseline = readJson(BASELINE_PATH);
const baselineWarnings = baseline.warnings || {};
const baselineMap = new Map(Object.entries(baselineWarnings));

const regressions = [];
for (const [key, currentCount] of currentMap.entries()) {
  const baselineCount = baselineMap.get(key) || 0;
  if (currentCount > baselineCount) {
    regressions.push({ key, baselineCount, currentCount, delta: currentCount - baselineCount });
  }
}

if (regressions.length > 0) {
  console.error('New ESLint warnings detected (beyond baseline):');
  for (const reg of regressions.sort((a, b) => b.delta - a.delta).slice(0, 50)) {
    console.error(`- ${reg.key}: baseline=${reg.baselineCount}, current=${reg.currentCount}`);
  }
  console.error(`Total warning regressions: ${regressions.length}`);
  process.exit(1);
}

console.log('ESLint warning baseline check passed (no new warnings).');
