/** Brand colours used to tint the icon container per platform. */
export const PLATFORM_COLORS: Record<string, string> = {
  discord: '#5865F2',
  slack: '#611F69',
  telegram: '#26A5E4',
  mattermost: '#0058CC',
};

const PLATFORM_ICONS: Record<string, React.FC<IconProps>> = {
  discord: DiscordIcon,
  slack: SlackIcon,
  telegram: TelegramIcon,
  mattermost: MattermostIcon,
};

/** Platform brand icon with a generic chat-bubble fallback. */
export const PlatformIcon: React.FC<{ provider?: string; className?: string }> = ({
  provider,
  className = 'w-5 h-5',
}) => {
  const Icon = provider ? PLATFORM_ICONS[provider.toLowerCase()] : undefined;
  if (Icon) return <Icon className={className} />;
  return <MessageSquare className={className} aria-hidden />;
};

export default PlatformIcon;
